#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn generate_toml_single_port() {
        let toml = generate_toml("my-tailscale-host", &[1080]);
        assert!(toml.contains("protocol = \"socks5\""));
        assert!(toml.contains("address = \"127.0.0.1:1080\""));
        assert!(toml.contains("label = \"ssh-my-tailscale-host-1080\""));
    }

    #[test]
    fn generate_toml_multiple_ports() {
        let toml = generate_toml("bastion", &[1080, 1081, 1082]);
        assert_eq!(toml.matches("[[tunnel]]").count(), 3);
        assert!(toml.contains("address = \"127.0.0.1:1080\""));
        assert!(toml.contains("address = \"127.0.0.1:1081\""));
        assert!(toml.contains("address = \"127.0.0.1:1082\""));
        assert!(toml.contains("label = \"ssh-bastion-1080\""));
        assert!(toml.contains("label = \"ssh-bastion-1081\""));
        assert!(toml.contains("label = \"ssh-bastion-1082\""));
    }

    #[test]
    fn generate_toml_empty_ports() {
        let toml = generate_toml("host", &[]);
        assert!(toml.contains("# Generated by: houdinny import ssh"));
        assert!(!toml.contains("[[tunnel]]"));
    }

    #[test]
    fn write_config_creates_new_file() {
        let dir = tempfile::tempdir().expect("tempdir");
        let path = dir.path().join("tunnels.toml");

        let toml_block = "[[tunnel]]\nprotocol = \"socks5\"\nlabel = \"test\"\n";
        write_config(&path, toml_block).expect("should write");

        let contents = std::fs::read_to_string(&path).expect("read");
        assert!(contents.contains("[proxy]"));
        assert!(contents.contains("listen = \"127.0.0.1:8080\""));
        assert!(contents.contains("[[tunnel]]"));
        assert!(contents.contains("label = \"test\""));
    }

    #[test]
    fn write_config_appends_to_existing() {
        let dir = tempfile::tempdir().expect("tempdir");
        let path = dir.path().join("tunnels.toml");

        // Create an initial file.
        std::fs::write(&path, "[proxy]\nlisten = \"0.0.0.0:9090\"\n").expect("write");

        let toml_block = "[[tunnel]]\nprotocol = \"socks5\"\nlabel = \"appended\"\n";
        write_config(&path, toml_block).expect("should append");

        let contents = std::fs::read_to_string(&path).expect("read");
        assert!(contents.contains("listen = \"0.0.0.0:9090\""));
        assert!(contents.contains("label = \"appended\""));
    }

    #[tokio::test]
    async fn import_ssh_empty_ports_errors() {
        let dir = tempfile::tempdir().expect("tempdir");
        let path = dir.path().join("tunnels.toml");
        let result = import_ssh("host", "user", &[], &path, false).await;
        assert!(result.is_err());
        assert!(
            result
                .unwrap_err()
                .to_string()
                .contains("no ports specified")
        );
    }
}
